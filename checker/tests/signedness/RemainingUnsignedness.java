public class RemainingUnsignedness {
    int returnIntWithLocalVariable(char c) {
        int i = c;
        return i;
    }

    long returnLongWithLocalVariable(char c) {
        long l = c;
        return l;
    }

    int returnIntWithoutLocalVariable(char c) {
        return c;
    }

    long returnLongWithoutLocalVariable(char c) {
        return c;
    }
}
